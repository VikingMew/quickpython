# 数据类型演示

# 1. 整数
print("=== 整数 ===")
x = 42
print(x)
print(-17)

# 2. 浮点数
print("\n=== 浮点数 ===")
pi = 3.14159
print(pi)
print(2.5 * 4.0)

# 3. 混合运算（自动类型提升）
print("\n=== 混合运算 ===")
result = 10 / 3.0
print(result)

# 4. 类型转换
print("\n=== 类型转换 ===")
a = 3.14
b = int(a)
print(b)

c = 42
d = float(c)
print(d)

# 5. 字符串
print("\n=== 字符串 ===")
s1 = "Hello"
s2 = "World"
greeting = s1 + " " + s2
print(greeting)

# 6. None
print("\n=== None ===")
x = None
print(x)
