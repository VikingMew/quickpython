# 基础功能演示

# 1. 简单表达式
print("=== 简单表达式 ===")
print(1 + 2)
print(10 - 3)
print(4 * 5)
print(20 / 4)

# 2. 变量
print("\n=== 变量 ===")
x = 10
y = 20
z = x + y
print(z)

# 3. 比较运算符
print("\n=== 比较运算符 ===")
print(5 == 5)
print(5 != 3)
print(10 > 5)
print(3 < 7)
print(5 >= 5)
print(3 <= 3)

# 4. 布尔值
print("\n=== 布尔值 ===")
print(True)
print(False)
a = True
b = False
print(a)
print(b)
